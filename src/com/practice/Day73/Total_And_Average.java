package com.practice.Day73;

public class Total_And_Average {
	public static void main(String[] args) {
		
		int a=65;
		int b=78;
		int c=90;
		int d=50;
		int total=a+b+c+d;
		System.out.println("Total of all the fields :: "+total);
		double avg=total/4;
		System.out.println("Average of all the fields :: "+avg);
	}

}
