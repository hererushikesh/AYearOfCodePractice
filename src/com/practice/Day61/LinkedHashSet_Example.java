package com.practice.Day61;

import java.util.LinkedHashSet;

public class LinkedHashSet_Example {
	public static void main(String[] args) {

		LinkedHashSet<String> ls = new LinkedHashSet<String>();
		ls.add("C++");
		ls.add("Java");
		ls.add("Scala");
		ls.add("Python");
		ls.add("Ruby");
		System.out.println(ls);
	}
}
